export type TranslationKey = keyof (typeof translations)["en"];

interface I18nContextValue {
  language: Language;
  calendar: CalendarType;
  dir: "ltr" | "rtl";
  t: (key: TranslationKey) => string;
  setLanguage: (lang: Language) => void;
  setCalendar: (cal: CalendarType) => void;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>("en");
  const [calendar, setCalendarState] = useState<CalendarType>("gregorian");

  // Hydrate from localStorage after mount (SSR-safe)
  useEffect(() => {
    const lang = localStorage.getItem("studbud:lang") as Language | null;
    const cal = localStorage.getItem("studbud:cal") as CalendarType | null;
    if (lang === "en" || lang === "fa") setLanguageState(lang);
    if (cal === "gregorian" || cal === "jalali") setCalendarState(cal);
  }, []);

  useEffect(() => {
    document.documentElement.dir = language === "fa" ? "rtl" : "ltr";
    document.documentElement.lang = language;
  }, [language]);

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      calendar,
      dir: language === "fa" ? "rtl" : "ltr",
      t: (key) => translations[language][key] ?? translations.en[key] ?? key,
      setLanguage: (lang) => {
        setLanguageState(lang);
        localStorage.setItem("studbud:lang", lang);
      },
      setCalendar: (cal) => {
        setCalendarState(cal);
        localStorage.setItem("studbud:cal", cal);
      },
    }),
    [language, calendar],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}